// Luo muuttuja 'age' ja anna sille kokonaislukuarvo
let age = 37;

// Luo muuttuja 'name' ja anna sille merkkijonoarvo
let name = "John";

// Luo muuttuja 'isStudent' ja anna sille totuusarvo (boolean)
let isStudent = true;

// Luo muuttuja 'hobbies' ja anna sille taulukko merkkijonoarvoja
let hobbies = ["programmin", "reading", "cycling", "bouldering"];

// Luo 'info'-objekti, jossa on kaksi ominaisuutta: 'schoolName' ja 'grade'
let info = {
    schoolName: "Business College",
    city: "Helsinki",
    grade: 1
};

// Tulosta jokainen muuttuja konsoliin
console.log("Age:", age);
console.log("Name:", name);
console.log("Is Student:", isStudent);
console.log("Hobbies:", hobbies);
console.log("Info:", info);

// Tarkista ja tulosta jokaisen muuttujan tietotyyppi
console.log("Type of age:", typeof age);
console.log("Type of name:", typeof name);
console.log("Type of isStudent:", typeof isStudent);
console.log("Type of hobbies:", typeof hobbies);
console.log("Type of info:", typeof info);




